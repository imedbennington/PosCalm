    void affiche_drinks (){
        try{
            
            st=cnx.obtenircon().createStatement();
            ResultSet rs=st.executeQuery("select name, category from drink");
            while(rs.next()){
                String name = rs.getString("name");
                String category = rs.getString("category");System.out.print(category);
                switch(category){
                    case "Hookah":
                        hookah.addItem(name);
                        hookah_cl.addItem(name);
                    break;
                    case "Food":
                        food.addItem(name);
                        food_cl.addItem(name);
                        break;
                    case "Coffee and tea":
                        jComboBox1.addItem(name);
                        drinks_list_cl.addItem(name);
                        break;
                    case "Soda":
                        water_soda.addItem(name);
                        water_list_cl.addItem(name);
                    break;
                    case "Water":
                        water_soda.addItem(name);
                        water_list_cl.addItem(name);
                        break;
                }  
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(rootPane,e);
        }
    }
